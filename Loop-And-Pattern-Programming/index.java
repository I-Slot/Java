class Loop{
    public static void main(String[] args)
    {
    //   System.out.println("Working.......");
    
    // for loop

    for(int i = 0; i < 10; i++)
    {
        //System.out.println("*");
    }

    // While loop
    int j = 0;
    while(j < 5)
    {
        // System.out.println("*");
        j++;
    }

    // do while loop
    int k = 10;
    do 
    {
        System.out.println("*");
        k++;
    } while(k < 5);
    }
}